package variable;

public class Var4 {

    public static void main(String[] args) {
        int a = 1;
        double b = 10.0;
        boolean c = true;
        char d = 'A'; // 실무에서 안쓴다
        String e = "Hello";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
